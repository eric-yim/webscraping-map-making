class USA_UTIL:
    @staticmethod
    def get_location_by_state(state):
        state = state.title()
        if not state in POP_CITIES:
            print(f"Unknown state {state}")
            return None
        city = POP_CITIES.get(state)
        if not city in CITY_COORDINATES:
            print(f"Unknown city {city}")
            return None
        return CITY_COORDINATES.get(city)
    
    @staticmethod
    def assert_state(state):
        assert state.title() in POP_CITIES, f"Unknown state {state}"


